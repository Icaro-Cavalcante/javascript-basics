// Math - Objeto embutido que fornece uma variedade de propriedades e métodos

// O número pi
pi = Math.PI
console.log(pi) // output: 3.141592653589793

// Para arredondar números
let z = Math.round(5.78995) 
console.log(z) // output: 6

// Arredondar
let x = Math.floor(2.9) // Arredondar para baixo
let y = Math.ceil(3.1) // Arredondar para cima
